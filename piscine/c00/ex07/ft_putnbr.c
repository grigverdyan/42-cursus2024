#include <unistd.h>

void	ft_putnbr(int nb);
void	ft_putchar(char c);

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long int	n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n / 10 == 0)
	{
		ft_putchar(n + '0');
		return ;
	}
	else
		ft_putnbr(n / 10);
	ft_putnbr(n % 10);
}
